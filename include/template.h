#ifndef TEMPLATE_H
#define TEMPLATE_H

/**
 * @brief  将两个整数相加。
 * 
 * @param a 第一个整数。
 * @param b 第二个整数。
 * @return  a 和 b 的和。
 */
int add(int a, int b);

/**
 * @brief  从第一个整数中减去第二个整数。
 * 
 * @param a 第一个整数。
 * @param b 要减去的整数。
 * @return  a - b 的结果。
 */
int subtract(int a, int b);

#endif // TEMPLATE_H 结束宏定义保护